package cours.ulaval.glo4003.model;

public class TempsConsacre {
	private int heuresCours = 0;
	private int heuresLabo = 0;
	private int heuresAutres = 0;

	public int getHeuresCours() {
		return heuresCours;
	}

	public void setHeuresCours(int heuresCours) {
		this.heuresCours = heuresCours;
	}

	public int getHeuresLabo() {
		return heuresLabo;
	}

	public void setHeuresLabo(int heuresLabo) {
		this.heuresLabo = heuresLabo;
	}

	public int getHeuresAutres() {
		return heuresAutres;
	}

	public void setHeuresAutres(int heuresAutres) {
		this.heuresAutres = heuresAutres;
	}

}
